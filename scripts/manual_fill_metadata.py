def main():
    with open('/tmp/books_parsed.pkl', 'rb') as f:
        books = pickle.load(f)
    
    filled = 0
    for book in books:
        title = book.get('title', '')
        if title in MANUAL_METADATA and not book.get('abstract'):
            meta = MANUAL_METADATA[title]
            book['abstract'] = meta['abstract']
            book['reason'] = meta['reason']
            book['quotes'] = meta['quotes']
            filled += 1
            print(f'✓ {title}')
        elif title in MANUAL_METADATA and book.get('abstract'):
            print(f'  已完整: {title}')
    
    print(f'\n补充完成: {filled} 本')
    
    # 保存
    with open('/tmp/books_parsed.pkl', 'wb') as f:
        pickle.dump(books, f)
    
    complete = [b for b in books if b.get('abstract') and b.get('reason') and b.get('quotes')]
    print(f'现完整书籍: {len(complete)} / {len(books)}')

if __name__ == '__main__':
    main()
